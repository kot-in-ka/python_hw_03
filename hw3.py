# Задача 1. Создайте файл. Запишите в него N первых элементов последовательности Фибоначчи.
# 6 –> 1 1 2 3 5 8
def task1():
    N1 = 1
    N2 = 1
    count = int(input("Введите колличество первых элементов последовательности Фибоначчи: "))
    data = open('fibonacci.txt', 'w')
    for i in range(count):
        data.writelines(str(N1) + '\n')
        (N1, N2) = (N2, N1 + N2)
    print(f'{count} первых элементов последовательности Фибоначчи записаны в Файл "fibonacci.txt"')
    data.close()

