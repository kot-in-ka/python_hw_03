# Задача 3. Создайте скрипт бота, который находит ответы на фразы по ключу в словаре. Бот должен, как минимум, отвечать на фразы «привет»,
# «как тебя зовут». Если фраза ему неизвестна, он выводит соответствующую фразу.
def task3():
    dict = \
        {
            'привет': 'И тебе привет',
            'как тебя зовут?': 'Меня зовут Геннадий',
            'ты кто': 'Я твой бот!',
            'как дела': 'Отлично!',
            'пока': 'Ну, пока',
            'выход': 'Ты заходи, если что.'
        }

    is_started = True
    while is_started:
        answer = input('Пользователь: ')
        if answer == 'выход':
            is_started = not is_started
        if answer in dict.keys():
            print('Бот:', dict[answer])
        else:
            print("Не могу тебя понять, повтори пожалуйста еще раз.")
